public class UserInformation {

    public String name;
    public String surname;
    public String pesel;
    public String phone;
    public String city;
    public String address;
    public String zip;


    public UserInformation(String name, String surname, String phone, String city, String address)
    {
        this.name = name;
        this.surname = surname;
        this.pesel = pesel;
        this.phone = phone;
        this.city = city;
        this.address = address;
        this.zip = zip;

    }


}
